#empty tuple
tup = ()
print(tup)
print(type(tup))

tup1 = (1)
tup2 = (1,)       
# correct syntax for tuple in case of single element
print(tup1)
print(type(tup1))       #type int in this case
print(tup2)
print(type(tup2))

tup1 = (1, 2, 3, 4,)
tup2 = (1, 2, 3, 4)
# in case of multiple elements either of them will do
print(tup1)
print(tup2)
