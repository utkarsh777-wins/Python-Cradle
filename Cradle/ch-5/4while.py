#print numbers 1 to 5
i = 1
while i <= 5 :
    print(i)    #looping i
    i += 1

print("LOOP ENDED")

#reversing the loop
i = 5
while i >= 1 :  
    print(i)
    i -= 1  #descending 5-1
print("End of LOOP")

