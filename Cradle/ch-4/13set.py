# Set in Python
"""
Set is the collection of the unordered items.
Each element in the set must be unique & immutable

"""
nums = { 1, 2, 3, 4}
set2 = { 1, 2, 2, 2}
print(nums)
print(set2)
#repeated elements stored only once, so it resolved to {1,2}
#no changes to any data type once assigned into the set, it includes boolean and tuples.
#list and dictionary, are mutable so they can't be stored in sets

#empty set syntax
null_set = set()    
